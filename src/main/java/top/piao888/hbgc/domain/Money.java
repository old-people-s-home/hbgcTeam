package top.piao888.hbgc.domain;

public class Money {
    private Long pid;

    private Long total;

    private Long cou;

    private Long pro;

    private Long city;

    private Long dist;

    private Long com;

    public Long getPid() {
        return pid;
    }

    public void setPid(Long pid) {
        this.pid = pid;
    }

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public Long getCou() {
        return cou;
    }

    public void setCou(Long cou) {
        this.cou = cou;
    }

    public Long getPro() {
        return pro;
    }

    public void setPro(Long pro) {
        this.pro = pro;
    }

    public Long getCity() {
        return city;
    }

    public void setCity(Long city) {
        this.city = city;
    }

    public Long getDist() {
        return dist;
    }

    public void setDist(Long dist) {
        this.dist = dist;
    }

    public Long getCom() {
        return com;
    }

    public void setCom(Long com) {
        this.com = com;
    }
}